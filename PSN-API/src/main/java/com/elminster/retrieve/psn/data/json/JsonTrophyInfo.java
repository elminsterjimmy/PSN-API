package com.elminster.retrieve.psn.data.json;

public class JsonTrophyInfo {

  private String stampDate;
  private String stampTime;
  private String trophyStamp;
  private String type;
  private String trophyWon;
  private int trophyId;
  private String title;
  private String imgUrl;
  private String desc;
  /**
   * @return the stampDate
   */
  public String getStampDate() {
    return stampDate;
  }
  /**
   * @param stampDate the stampDate to set
   */
  public void setStampDate(String stampDate) {
    this.stampDate = stampDate;
  }
  /**
   * @return the stampTime
   */
  public String getStampTime() {
    return stampTime;
  }
  /**
   * @param stampTime the stampTime to set
   */
  public void setStampTime(String stampTime) {
    this.stampTime = stampTime;
  }
  /**
   * @return the trophyStamp
   */
  public String getTrophyStamp() {
    return trophyStamp;
  }
  /**
   * @param trophyStamp the trophyStamp to set
   */
  public void setTrophyStamp(String trophyStamp) {
    this.trophyStamp = trophyStamp;
  }
  /**
   * @return the type
   */
  public String getType() {
    return type;
  }
  /**
   * @param type the type to set
   */
  public void setType(String type) {
    this.type = type;
  }
  /**
   * @return the trophyWon
   */
  public String getTrophyWon() {
    return trophyWon;
  }
  /**
   * @param trophyWon the trophyWon to set
   */
  public void setTrophyWon(String trophyWon) {
    this.trophyWon = trophyWon;
  }
  /**
   * @return the trophyId
   */
  public int getTrophyId() {
    return trophyId;
  }
  /**
   * @param trophyId the trophyId to set
   */
  public void setTrophyId(int trophyId) {
    this.trophyId = trophyId;
  }
  /**
   * @return the title
   */
  public String getTitle() {
    return title;
  }
  /**
   * @param title the title to set
   */
  public void setTitle(String title) {
    this.title = title;
  }
  /**
   * @return the imgUrl
   */
  public String getImgUrl() {
    return imgUrl;
  }
  /**
   * @param imgUrl the imgUrl to set
   */
  public void setImgUrl(String imgUrl) {
    this.imgUrl = imgUrl;
  }
  /**
   * @return the desc
   */
  public String getDesc() {
    return desc;
  }
  /**
   * @param desc the desc to set
   */
  public void setDesc(String desc) {
    this.desc = desc;
  }
}
