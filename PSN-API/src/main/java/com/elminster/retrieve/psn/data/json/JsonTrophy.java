package com.elminster.retrieve.psn.data.json;

public class JsonTrophy {

  private int bronze;
  private int gold;
  private int platinum;
  private int silver;
  /**
   * @return the bronze
   */
  public int getBronze() {
    return bronze;
  }
  /**
   * @param bronze the bronze to set
   */
  public void setBronze(int bronze) {
    this.bronze = bronze;
  }
  /**
   * @return the gold
   */
  public int getGold() {
    return gold;
  }
  /**
   * @param gold the gold to set
   */
  public void setGold(int gold) {
    this.gold = gold;
  }
  /**
   * @return the platinum
   */
  public int getPlatinum() {
    return platinum;
  }
  /**
   * @param platinum the platinum to set
   */
  public void setPlatinum(int platinum) {
    this.platinum = platinum;
  }
  /**
   * @return the silver
   */
  public int getSilver() {
    return silver;
  }
  /**
   * @param silver the silver to set
   */
  public void setSilver(int silver) {
    this.silver = silver;
  }
}
